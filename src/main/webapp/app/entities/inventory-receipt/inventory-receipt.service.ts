import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { type IInventoryReceipt } from '@/shared/model/inventory-receipt.model';

const baseApiUrl = 'api/inventory-receipts';

export default class InventoryReceiptService {
  find(id: number): Promise<IInventoryReceipt> {
    return new Promise<IInventoryReceipt>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  create(entity: IInventoryReceipt): Promise<IInventoryReceipt> {
    return new Promise<IInventoryReceipt>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  update(entity: IInventoryReceipt): Promise<IInventoryReceipt> {
    return new Promise<IInventoryReceipt>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  partialUpdate(entity: IInventoryReceipt): Promise<IInventoryReceipt> {
    return new Promise<IInventoryReceipt>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
